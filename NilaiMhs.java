    public void isiFields() {
        mahasiswa p = (mahasiswa) record.get(baris);
        txtMhs.setText(String.valueOf(p.getNama()));
        txtTM.setText(String.valueOf(p.getTgsM()));
        txtTP.setText(String.valueOf(p.getTgsP()));
        txtUTS.setText(String.valueOf(p.getUts()));
        txtUAS.setText(String.valueOf(p.getUas()));
        txtKeh.setText(String.valueOf(p.getKehadiran()));
    }
    
    public void ambil_data_tabel() {

        tabel.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                baris = tabel.getSelectedRow();
                if (baris != -1) {
                    isiFields();
                }
            }
        });
    }
    
