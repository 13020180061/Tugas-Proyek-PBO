    private String calculate(String... utm){
        for(int i=0; i<utm.length; i++){
            if(utm[i].isEmpty()){
                utm[i] = "0";
            }
        }
        double t1=0, t2=0, t3=0, t4=0, t5=0;
        for (int i=0; i<utm.length; i++){
            double cv = Double.valueOf(utm[i]);
            switch(i){
                case 0:
                    t1=cv*0.2;
                    break;
                case 1:
                    t2=cv*0.2;
                    break;
                case 2:
                    t3=cv*0.2;
                    break;
                case 3:
                    t4=cv*0.25;
                    break;
                case 4:
                    t5=cv*0.15;
                    break;
                default:
                    break;
            }
        }
        double jumlah=t1+t2+t3+t4+t5;
        double as = Math.round(jumlah);
        String cvJumlah = indexOF(as);
        return cvJumlah;
    }
    
    public String indexOF(double b){
        String hsl;
        if(b%1==0){
            hsl=String.valueOf(b).substring(0, String.valueOf(b).lastIndexOf("."));
        }else{
            hsl=String.valueOf(b);
        }
        return hsl;
    }
    
    private String calAbjad(String jumlah){
        String nilai="";
        double b=Double.valueOf(jumlah);
        if(b>85){
            nilai="A";
        }else if(b<=85 && b>=81){
            nilai="A-";
        }else if(b<=80 && b>=76){
            nilai="B+";
        }else if(b<=75 && b>=71){
            nilai="B";
        }else if(b<=70 && b>=66){
            nilai="B-";
        }else if(b<=65 && b>=61){
            nilai="C+";
        }else if(b<=60 && b>=51){
            nilai="C";
        }else if(b<=50 && b>=45){
            nilai="D";
        }else if(b<45){
            nilai="E";
        }
        return nilai;
    }
    
