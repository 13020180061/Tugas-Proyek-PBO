    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        int[] pilih = tabel.getSelectedRows();
        if(pilih.length > 0){
            for(int i = pilih.length-1; i >=0; i--){
                model.removeRow(pilih[i]);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Data yang ingin Dihapus");
        }
        reset();
    }//GEN-LAST:event_HapusActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        try {
            // TODO add your handling code here:
            mahasiswa p = new mahasiswa();

            if (txtMhs.getText().equals("") && txtTM.getText().equals("") && txtTP.getText().equals("") && txtUTS.getText().equals("") && txtUAS.getText().equals("") && txtKeh.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Silahkan Isi Data Yang Akan Diubah");
            } else {
                p.setNama(txtMhs.getText());
                p.setTgsM(Integer.parseInt(txtTM.getText()));
                p.setTgsP(Integer.parseInt(txtTP.getText()));
                p.setUts(Integer.parseInt(txtUTS.getText()));
                p.setUas(Integer.parseInt(txtUAS.getText()));
                p.setKehadiran(Integer.parseInt(txtKeh.getText()));
                profileServices.update(p);
                ambil_data_tabel();
                JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Data Gagal Diubah");
        }
    }//GEN-LAST:event_EditActionPerformed

